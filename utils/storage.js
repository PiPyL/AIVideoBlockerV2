  async cacheCaptionText(key, result) {
    if (!key) return;

    const cache = await this.getCaptionCache();
    cache[key] = {
      ...result,
      timestamp: Date.now()
    };

    const entries = Object.entries(cache);
    if (entries.length > 1000) {
      const sorted = entries.sort((a, b) => a[1].timestamp - b[1].timestamp);
      await chrome.storage.local.set({ captionCache: Object.fromEntries(sorted.slice(-800)) });
    } else {
      await chrome.storage.local.set({ captionCache: cache });
    }
  },

  async getGeminiCache() {
    try {
      const data = await chrome.storage.local.get('geminiCache');
      return data.geminiCache || {};
    } catch (e) {
      return {};
    }
  },

  async getGeminiCacheEntry(key) {
    if (!key) return null;
    const cache = await this.getGeminiCache();
    return cache[key] || null;
  },

  async getGeminiCacheEntries(keys = []) {
    const cache = await this.getGeminiCache();
    const result = {};
    for (const key of keys) {
      if (cache[key]) result[key] = cache[key];
    }
    return result;
  },

  async cacheGeminiResult(key, result) {
    if (!key) return;

    const cache = await this.getGeminiCache();
    cache[key] = {
      ...result,
      timestamp: Date.now()
    };

    const entries = Object.entries(cache);
    if (entries.length > 3000) {
      const sorted = entries.sort((a, b) => (a[1].timestamp || 0) - (b[1].timestamp || 0));
      await chrome.storage.local.set({ geminiCache: Object.fromEntries(sorted.slice(-2400)) });
    } else {
      await chrome.storage.local.set({ geminiCache: cache });
    }
  },

  async clearGeminiCache() {
    await chrome.storage.local.set({ geminiCache: {} });
    return { ok: true };
  },

  /**
   * Tăng counter thống kê
   */
  async incrementStat(key) {
    const settings = await this.getSettings();
    settings.stats[key] = (settings.stats[key] || 0) + 1;
    settings.stats.lastActive = Date.now();
    await this.updateSettings({ stats: settings.stats });
  },

  /**
   * Cập nhật thống kê theo batch để tránh ghi storage quá nhiều.
   * @param {object} payload
   */
  async updateDetectionStats(payload = {}) {
    const settings = await this.getSettings();
    const stats = settings.stats || {};
    const context = payload.context || 'other';
    const method = payload.method || 'none';
    const signalCounts = payload.signalCounts || {};

    stats.totalScanned = (stats.totalScanned || 0) + (payload.scanned || 0);
    stats.totalBlocked = (stats.totalBlocked || 0) + (payload.blocked || 0);
    stats.lastActive = Date.now();

    if (!stats.byContext) stats.byContext = {};
    if (!stats.byContext[context]) stats.byContext[context] = { scanned: 0, blocked: 0 };
    stats.byContext[context].scanned += payload.scanned || 0;
    stats.byContext[context].blocked += payload.blocked || 0;

    if (!stats.byMethod) stats.byMethod = {};
    stats.byMethod[method] = (stats.byMethod[method] || 0) + (payload.blocked || 0);

    if (!stats.bySignal) stats.bySignal = {};
    for (const level of ['strong', 'medium', 'weak']) {
      const value = Number(signalCounts[level] || 0);
      stats.bySignal[level] = (stats.bySignal[level] || 0) + value;
    }

    if (!stats.byRiskLevel) stats.byRiskLevel = {};
    const riskLevel = payload.riskLevel || 'safe';
    stats.byRiskLevel[riskLevel] = (stats.byRiskLevel[riskLevel] || 0) + (payload.scanned || 0);

    if (!stats.byRiskCategory) stats.byRiskCategory = {};
    for (const category of payload.riskCategories || []) {
      stats.byRiskCategory[category] = (stats.byRiskCategory[category] || 0) + (payload.blocked || 0);
    }

    await this.updateSettings({ stats });
    return stats;
  },

  /**
   * Kiểm tra password phụ huynh
   */
  async verifyPassword(input) {
    const settings = await this.getSettings();
    if (!settings.parentalPassword) return true;
    return input === settings.parentalPassword;
  },

  /**
   * Xác định context trang YouTube hiện tại.
   */
  getYouTubeContext(pathname = '') {
    if (pathname === '/watch') return 'watch';
    if (pathname.startsWith('/results')) return 'search';
    if (pathname.startsWith('/shorts')) return 'shorts';
    if (pathname.startsWith('/@') || pathname.startsWith('/channel/') || pathname.startsWith('/c/')) return 'channel';
    if (pathname === '/' || pathname.startsWith('/feed/')) return 'home';
    return 'other';
  },

  /**
   * Kiểm tra channel có trong whitelist
   */
  async isWhitelisted(channelId) {
    const settings = await this.getSettings();
    return settings.whitelistedChannels.includes(channelId);
  },

  _mergeStats(storedStats = {}) {
    const defaults = this.DEFAULT_SETTINGS.stats;
    return {
      ...defaults,
      ...storedStats,
      byContext: { ...defaults.byContext, ...(storedStats.byContext || {}) },
      byMethod: { ...defaults.byMethod, ...(storedStats.byMethod || {}) },
      bySignal: { ...defaults.bySignal, ...(storedStats.bySignal || {}) },
      byRiskLevel: { ...defaults.byRiskLevel, ...(storedStats.byRiskLevel || {}) },
      byRiskCategory: { ...defaults.byRiskCategory, ...(storedStats.byRiskCategory || {}) }
    };
  },

  _sanitizeSettingsPartial(partial = {}) {
    const sanitized = { ...(partial || {}) };
    if (sanitized.gemini) {
      sanitized.gemini = this._sanitizeGeminiSettings(sanitized.gemini);
    }
    if (sanitized.openrouter) {
      sanitized.openrouter = this._sanitizeOpenRouterSettings(sanitized.openrouter);
    }
    return sanitized;
  },

  _sanitizeGeminiSettings(gemini = {}) {
    const source = { ...(gemini || {}) };
    const hasApiKey = Boolean(source.hasApiKey || source.apiKey);
    delete source.apiKey;
    return {
      ...this.DEFAULT_SETTINGS.gemini,
      ...source,
      hasApiKey
    };
  },

  _sanitizeOpenRouterSettings(openrouter = {}) {
    const source = { ...(openrouter || {}) };
    const hasApiKey = Boolean(source.hasApiKey || source.apiKey);
    delete source.apiKey;
    return {
      ...this.DEFAULT_SETTINGS.openrouter,
      ...source,
      hasApiKey
    };
  }
