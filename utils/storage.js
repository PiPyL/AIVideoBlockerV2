  /**
   * Lấy settings hiện tại, merge với defaults
   */
  async getSettings() {
    try {
      const data = await chrome.storage.local.get('settings');
      const storedSettings = data.settings || {};
      const storedVersion = Number(storedSettings.detectorVersion || 0);
      const settings = { ...this.DEFAULT_SETTINGS, ...storedSettings };
      delete settings.blockMode;
      settings.aiToolPatterns = this.DEFAULT_SETTINGS.aiToolPatterns;
      settings.childRiskPatterns = this.DEFAULT_SETTINGS.childRiskPatterns;
      settings.captionScan = { ...this.DEFAULT_SETTINGS.captionScan, ...(storedSettings.captionScan || {}) };
      settings.gemini = this._sanitizeGeminiSettings(storedSettings.gemini);
      settings.openrouter = this._sanitizeOpenRouterSettings(storedSettings.openrouter);
      if (!settings.activeProvider) settings.activeProvider = this.DEFAULT_SETTINGS.activeProvider;
      settings.stats = this._mergeStats(storedSettings.stats);
      if (storedVersion < this.DEFAULT_SETTINGS.detectorVersion) {
        settings.detectorVersion = this.DEFAULT_SETTINGS.detectorVersion;
        settings.aiKeywords = this.DEFAULT_SETTINGS.aiKeywords;
        settings.syntheticKeywords = this.DEFAULT_SETTINGS.aiKeywords;
        settings.childRiskKeywords = this.DEFAULT_SETTINGS.childRiskKeywords;
      }
      if (!settings.syntheticKeywords) settings.syntheticKeywords = settings.aiKeywords || this.DEFAULT_SETTINGS.aiKeywords;
      if (!settings.childRiskKeywords) settings.childRiskKeywords = this.DEFAULT_SETTINGS.childRiskKeywords;
      return settings;
    } catch (e) {
      console.warn('[AIBlocker] Storage read error:', e);
      return {
        ...this.DEFAULT_SETTINGS,
        syntheticKeywords: this.DEFAULT_SETTINGS.aiKeywords,
        gemini: this._sanitizeGeminiSettings(),
        stats: this._mergeStats()
      };
    }
  },

  /**
   * Cập nhật settings
   */
  async updateSettings(partial) {
    const current = await this.getSettings();
    const cleanedPartial = this._sanitizeSettingsPartial(partial);
    const updated = { ...current, ...cleanedPartial };
    if (cleanedPartial.gemini) {
      updated.gemini = this._sanitizeGeminiSettings({
        ...current.gemini,
        ...cleanedPartial.gemini
      });
    }
    if (cleanedPartial.openrouter) {
      updated.openrouter = this._sanitizeOpenRouterSettings({
        ...current.openrouter,
        ...cleanedPartial.openrouter
      });
    }
    const persistableSettings = { ...updated };
    delete persistableSettings.aiToolPatterns;
    delete persistableSettings.childRiskPatterns;
    delete persistableSettings.blockMode;
    persistableSettings.gemini = this._sanitizeGeminiSettings(persistableSettings.gemini);
    persistableSettings.openrouter = this._sanitizeOpenRouterSettings(persistableSettings.openrouter);
    await chrome.storage.local.set({ settings: persistableSettings });
    return updated;
  },

  /**
   * Lấy cache video đã scan
   */
  async getVideoCache() {
    try {
      const data = await chrome.storage.local.get('videoCache');
      return data.videoCache || {};
    } catch (e) {
      return {};
    }
  },

  /**
   * Cache kết quả scan video
   * @param {string} videoId
   * @param {object} result - { isAI, confidence, method, timestamp }
   */
  async cacheVideoResult(videoId, result) {
    const cache = await this.getVideoCache();
    cache[videoId] = {
      ...result,
      timestamp: Date.now()
    };

    // Giới hạn cache 5000 entries
    const entries = Object.entries(cache);
    if (entries.length > 5000) {
      const sorted = entries.sort((a, b) => a[1].timestamp - b[1].timestamp);
      const trimmed = Object.fromEntries(sorted.slice(-4000));
      await chrome.storage.local.set({ videoCache: trimmed });
    } else {
      await chrome.storage.local.set({ videoCache: cache });
    }
  },

